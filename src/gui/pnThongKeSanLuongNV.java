    private void docDLVaoTable() {
        LocalDate ngayLam = convertToLocalDateViaInstant(dateTim.getSelectedDate().getTime());
        dsBangCCSP = bangChamCongSP_DAO.getAllDsCCSP(ngayLam);
        if (dsBangCCSP.isEmpty()) {
            txtSLCa.setText("0");
            txtSLTangCa.setText("0");
            txtTongSL.setText("0");
            txtTbSLCa.setText("0");
            txtTbSLTangCa.setText("0");
            return;
        }
        Collections.sort(dsBangCCSP, (o1, o2) -> o1.getBangDiemDanhCN().getBangPhanCong().getCongNhan().getMaCN().compareTo(o2.getBangDiemDanhCN().getBangPhanCong().getCongNhan().getMaCN()));
        int ca = 0;
        int tangCa = 0;
        int tongCa = 0;
        int tongTangCa = 0;
        int soCN = 0;
        BangChamCongSP bangCCSP;
        CongNhan cn = dsBangCCSP.get(0).getBangDiemDanhCN().getBangPhanCong().getCongNhan();
        String maCN = cn.getMaCN();

        for (int i = 0; i < dsBangCCSP.size();) {
            bangCCSP = dsBangCCSP.get(i);
            cn = bangCCSP.getBangDiemDanhCN().getBangPhanCong().getCongNhan();
//            JOptionPane.showMessageDialog(null, bangCCSP.getBangDiemDanhCN().getNgayLamViec().getNgayLam().getDayOfMonth() + " " + maCN);
            if (!maCN.equals(cn.getMaCN())) {
                modelThongKe.addRow(new Object[]{maCN, dsBangCCSP.get(i-1).getBangDiemDanhCN().getBangPhanCong().getCongNhan().getTenCN(), ca, tangCa, ca + tangCa});
                tongCa += ca;
                tongTangCa += tangCa;
                ca = 0;
                tangCa = 0;
                if (++i == dsBangCCSP.size()) {
                    break;
                }
                cn = dsBangCCSP.get(i).getBangDiemDanhCN().getBangPhanCong().getCongNhan();
                maCN = cn.getMaCN();
                soCN++;
                continue;
            }
            if (bangCCSP.getCaLam() != 3) {
                ca += bangCCSP.getSoLuongSP();
            } else {
                if (bangChamCongSP_DAO.ktraCoTangCa(bangCCSP.getBangDiemDanhCN().getBangPhanCong().getCongNhan().getMaCN(), bangCCSP.getBangDiemDanhCN().getNgayLamViec().getNgayLam())) {
                    tangCa += bangCCSP.getSoLuongSP();
                } else {
                    ca += bangCCSP.getSoLuongSP();
                }
            }
            i++;
        }
        modelThongKe.addRow(new Object[]{cn.getMaCN(), cn.getTenCN(), ca, tangCa, ca + tangCa});
        tongCa += ca;
        tongTangCa += tangCa;
        txtSLCa.setText(tongCa + "");
        txtSLTangCa.setText(tongTangCa + "");
        txtTongSL.setText(tongCa + tongTangCa + "");
        txtTbSLCa.setText((double) tongCa / soCN + "");
        txtTbSLTangCa.setText((double) tongTangCa / soCN + "");
    }

    private void xoaDLTableTK() {
        DefaultTableModel model = (DefaultTableModel) tableThongKe.getModel();
        model.setRowCount(0);
    }

    public LocalDate convertToLocalDateViaInstant(Date dateToConvert) {
        return dateToConvert.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
    }

    public LocalDate convertToLocalDateViaInstant(java.util.Date dateToConvert) {
        return dateToConvert.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
    }

    public java.util.Date convertToDateViaInstant(LocalDate date) {
        return Date.from(date.atStartOfDay().atZone(ZoneId.systemDefault()).toInstant());
    }

