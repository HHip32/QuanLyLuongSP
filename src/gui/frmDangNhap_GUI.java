    public void login() {
        if (kiemTraDuLieu()) {
            NhanVien nv = nhanVien_DAO.getNhanVienCoTK(txtTenDN.getText().trim());
            if (nv != null && pwdPass.getText().equals(nv.getMatKhau())) {
                this.setVisible(false);
                MenuChinh mnc = new MenuChinh(nv);
                mnc.setVisible(true);
            } else {
                sh_Mes("Tên đăng nhập hoặc mật khẩu không chính xác!", null);
            }
        }
    }


