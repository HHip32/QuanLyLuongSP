    public boolean kiemTraDuLieu() {
        if (txtTenDN.getText().trim().equals("")) {
            sh_Mes("Tên đăng nhập không được rỗng!", txtTenDN);
            return false;
        }
        if (pwdPass.getText().trim().equals("")) {
            sh_Mes("Mật khẩu không được rỗng!", null);
            return false;
        }
        return true;
    }

